package com.conditionalStatements;

/*
 * here we are checking if elseif conditional statements.
 */
public class Demoifelseif {

	public static void main(String[] args) {
		int a = 50, b = 55, c = 50;

		if (a == b) {
			System.out.println("a and b values are same. ");
		} else if (a == c) {
			System.out.println("a and c values are same. ");
		} else {
			System.out.println("a,b  and c values are not same. ");
		}

	}

}
